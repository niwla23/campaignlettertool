			<h3 class="font-semibold italic pt-2">"Frühsexualisierung"</h3>

			Dieser Begriff wird häufig in konservativen und rechtspopulistischen Debatten verwendet, um
			gegen sexualpädagogische Angebote in Kindergärten und Schulen zu argumentieren. Tatsächlich
			handelt es sich bei der Sexualerziehung in Bildungseinrichtungen um altersgerechte,
			wissenschaftlich fundierte Programme, die Kinder stärken, aufklären und vor sexualisierter
			Gewalt schützen sollen. Der Begriff „Frühsexualisierung“ ist kein fachlich anerkannter
			Terminus und wird meist ideologisch eingesetzt, um Unsicherheit und Ablehnung gegenüber
			moderner Pädagogik zu schüren.

			<h3 class="font-semibold italic pt-4">"Gender-Propaganda"</h3>

			Mit dem Begriff „Gender-Propaganda“ wird pauschal jede Form von Gleichstellungspolitik,
			geschlechtlicher Vielfalt oder geschlechtersensibler Bildung delegitimiert. Die Bezeichnung
			stammt nicht aus dem wissenschaftlichen Diskurs, sondern aus politischen Kontexten, die sich
			gegen die Anerkennung und Sichtbarkeit von trans*, nicht-binären und intergeschlechtlichen
			Menschen richten. Ziel ist meist die Ablehnung von Bildungsinhalten, die Vielfalt als
			gesellschaftliche Realität vermitteln.

			<h3 class="font-semibold italic pt-4">"Pädophilie-Verharmlosung"</h3>

			Dieser schwerwiegende Vorwurf wird von rechtsextremen Kreisen häufig pauschal gegen queere
			Personen oder sexualpädagogische Konzepte erhoben, obwohl es dafür keinerlei Belege gibt.
			Fachgesellschaften und Kinderschutzorganisationen warnen vor solchen falschen Zuschreibungen,
			da sie nicht nur stigmatisierend sind, sondern auch vom tatsächlichen Schutzauftrag ablenken.
			Statt differenzierter Präventionsarbeit wird Angst geschürt – zu Lasten betroffener Kinder und
			Fachkräfte.

			<h3 class="font-semibold italic pt-4">"Lebensschutz"</h3>

			Der Begriff „Lebensschutz“ wird in politischen Kontexten vor allem in Bezug auf Abtreibung
			verwendet. In konservativen und radikalen „Pro-Life“-Kreisen steht er oft für die Ablehnung
			des Selbstbestimmungsrechts über den eigenen Körper. Die Verbindung dieses Begriffes mit
			Kinderschutz zielt häufig darauf ab, reproduktive Rechte einzuschränken und moralische
			Bewertungen in politische Debatten zu tragen, die eigentlich medizinisch und sozialethisch
			differenziert geführt werden müssten.
